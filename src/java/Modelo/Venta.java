package Modelo;

/**
 *
 * @author Ale
 */
public class Venta {
    Integer id;
    Integer item;
    Integer idC;
    Integer idP;
    String nSerie;
    String nomPro;
    String descripcion;
    String fecha;
    Double precio;
    Integer cant;
    Double subtotal;
    Double monto;
    String estado;

    public Venta() {
    }

    public Venta(Integer id, Integer item, Integer idC, Integer idP, String nSerie, String nomPro, String descripcion, String fecha, Double precio, Integer cant, Double subtotal, Double monto, String estado) {
        this.id = id;
        this.item = item;
        this.idC = idC;
        this.idP = idP;
        this.nSerie = nSerie;
        this.nomPro = nomPro;
        this.descripcion = descripcion;
        this.fecha = fecha;
        this.precio = precio;
        this.cant = cant;
        this.subtotal = subtotal;
        this.monto = monto;
        this.estado = estado;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getItem() {
        return item;
    }

    public void setItem(Integer item) {
        this.item = item;
    }

    public Integer getIdC() {
        return idC;
    }

    public void setIdC(Integer idC) {
        this.idC = idC;
    }

    public Integer getIdP() {
        return idP;
    }

    public void setIdP(Integer idP) {
        this.idP = idP;
    }

    public String getnSerie() {
        return nSerie;
    }

    public void setnSerie(String nSerie) {
        this.nSerie = nSerie;
    }

    public String getNomPro() {
        return nomPro;
    }

    public void setNomPro(String nomPro) {
        this.nomPro = nomPro;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    
    

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public Double getPrecio() {
        return precio;
    }

    public void setPrecio(Double precio) {
        this.precio = precio;
    }

    public Integer getCant() {
        return cant;
    }

    public void setCant(Integer cant) {
        this.cant = cant;
    }

    public Double getSubtotal() {
        return subtotal;
    }

    public void setSubtotal(Double subtotal) {
        this.subtotal = subtotal;
    }

    public Double getMonto() {
        return monto;
    }

    public void setMonto(Double monto) {
        this.monto = monto;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
}
